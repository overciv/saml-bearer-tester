// ─── Terraform generation ─────────────────────────────────────────────────────

function _tfName(label) {
  return (label || 'app').toLowerCase()
    .replace(/[^a-z0-9]+/g, '_').replace(/^[0-9_]+/, '').replace(/_+$/, '').slice(0, 50) || 'app';
}

function _tfList(arr) {
  if (!arr?.length) return '[]';
  return `[${arr.map(v => `"${v}"`).join(', ')}]`;
}

function _tfEsc(s) {
  // Escape for HCL string. Dollar must become $$ so Okta exprs like ${user.email}
  // survive as literals. JS replace '$$$$' → produces '$$' in the output string.
  return String(s || '').replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\$/g, '$$$$');
}

function _parseDomain(domain) {
  const parts = (domain || 'yourorg.okta.com').split('.');
  return { orgName: parts[0] || 'yourorg', baseUrl: parts.slice(1).join('.') || 'okta.com' };
}

function downloadTerraform() {
  const label    = val('appLabel') || 'My App';
  const owner    = val('appOwner');
  const domain   = val('adminDomain') || 'yourorg.okta.com';
  const { orgName, baseUrl } = _parseDomain(domain);
  const resName  = _tfName(label);
  const proto    = currentProtocol;
  const now      = new Date().toISOString().slice(0, 10);

  const header = `# ─────────────────────────────────────────────────────────────────────────────
# Generated by Okta OAuth Super Tester
# App      : ${label}
# Protocol : ${proto === 'oidc' ? 'OIDC (OpenID Connect) — okta_app_oauth' : 'SAML 2.0 — okta_app_saml'}
# Domain   : ${domain}
# Generated: ${now}
# Reference: https://registry.terraform.io/providers/okta/okta/latest/docs
# ─────────────────────────────────────────────────────────────────────────────

terraform {
  required_providers {
    okta = {
      source  = "okta/okta"
      version = "~> 4.0"
    }
  }
}

# ─── Provider ─────────────────────────────────────────────────────────────────
# Recommended: use environment variables so credentials are never in source code:
#   export OKTA_ORG_NAME="${orgName}"
#   export OKTA_BASE_URL="${baseUrl}"
#   export OKTA_API_TOKEN="00YXSE_..."
# Then run: terraform init && terraform apply

variable "okta_api_token" {
  type        = string
  sensitive   = true
  description = "Okta SSWS API token (Admin Console → Security → API → Tokens)"
}

provider "okta" {
  org_name  = "${orgName}"
  base_url  = "${baseUrl}"
  api_token = var.okta_api_token
}

`;

  const appBlock  = proto === 'oidc' ? _buildOidcTf(resName, label) : _buildSamlTf(resName, label);
  const ownerBlock = owner ? _buildOwnerTf(resName, owner, proto) : '';
  const outputs   = _buildOutputsTf(resName, proto);

  const tf = header + appBlock + ownerBlock + outputs;

  const blob = new Blob([tf], { type: 'text/plain' });
  const a = document.createElement('a');
  a.href = URL.createObjectURL(blob);
  a.download = `${resName}.tf`;
  a.click();
  URL.revokeObjectURL(a.href);
  toast(`Downloaded ${resName}.tf`, 'success');
}

function _buildOidcTf(resName, label) {
  const grantTypes = [];
  if (document.getElementById('grant_authcode')?.checked)   grantTypes.push('authorization_code');
  if (document.getElementById('grant_refresh')?.checked)    grantTypes.push('refresh_token');
  if (document.getElementById('grant_creds')?.checked)      grantTypes.push('client_credentials');
  if (document.getElementById('grant_device')?.checked)     grantTypes.push('urn:ietf:params:oauth:grant-type:device_code');
  if (document.getElementById('grant_tokex')?.checked)      grantTypes.push('urn:ietf:params:oauth:grant-type:token-exchange');
  if (document.getElementById('grant_saml_grant')?.checked) grantTypes.push('urn:ietf:params:oauth:grant-type:saml2-bearer');

  const responseTypes  = grantTypes.includes('authorization_code') ? ['code'] : [];
  const redirectUris   = val('appRedirectUris').split('\n').map(s => s.trim()).filter(Boolean);
  const authMethod     = val('appAuthMethod') || 'client_secret_basic';
  const appType        = val('appType') || 'web';
  const redirectsLine  = redirectUris.length ? `  redirect_uris             = ${_tfList(redirectUris)}\n` : '';

  return `# ─── OIDC Application ────────────────────────────────────────────────────────
# Docs: https://registry.terraform.io/providers/okta/okta/latest/docs/resources/app_oauth
resource "okta_app_oauth" "${resName}" {
  label  = "${_tfEsc(label)}"
  type   = "${appType}"
  status = "ACTIVE"

  grant_types               = ${_tfList(grantTypes)}
  response_types            = ${_tfList(responseTypes)}
  token_endpoint_auth_method = "${authMethod}"
${redirectsLine}}

`;
}

function _buildSamlTf(resName, label) {
  const acsUrl    = val('samlAcsUrl') || 'https://sp.example.com/saml/acs';
  const audience  = val('samlAudience') || 'https://sp.example.com/metadata';
  const recipient = val('samlRecipient') || acsUrl;
  const nameIdFmt = val('samlNameIdFormat') || 'urn:oasis:names:tc:SAML:1.1:nameid-format:emailAddress';
  const nameIdTpl = val('samlNameIdTemplate') || '${user.userName}';
  const attrs     = collectSamlAttrs();

  const attrBlocks = attrs.map(a => `
  attribute_statements {
    name      = "${_tfEsc(a.name)}"
    namespace = "${a.namespace}"
    type      = "EXPRESSION"
    values    = ${_tfList(a.values)}
  }`).join('');

  return `# ─── SAML 2.0 Application ────────────────────────────────────────────────────
# Docs: https://registry.terraform.io/providers/okta/okta/latest/docs/resources/app_saml
resource "okta_app_saml" "${resName}" {
  label  = "${_tfEsc(label)}"
  status = "ACTIVE"

  # Service Provider endpoints
  sso_url     = "${_tfEsc(acsUrl)}"
  recipient   = "${_tfEsc(recipient)}"
  destination = "${_tfEsc(acsUrl)}"
  audience    = "${_tfEsc(audience)}"

  # Subject / NameID
  # Note: Okta expressions like \${user.userName} must be written as $\${user.userName} in HCL
  subject_name_id_template = "${_tfEsc(nameIdTpl)}"
  subject_name_id_format   = "${nameIdFmt}"

  # Signing
  signature_algorithm  = "RSA_SHA256"
  digest_algorithm     = "SHA256"
  response_signed      = true
  assertion_signed     = true
  honor_force_authn    = true
${attrBlocks}
}

`;
}

function _buildOwnerTf(resName, ownerLogin, proto) {
  const resType = proto === 'oidc' ? 'okta_app_oauth' : 'okta_app_saml';

  return `# ─── App Owner ────────────────────────────────────────────────────────────────
# Assigns APP_ADMIN role to the owner then scopes it to this app only.
# Docs: https://registry.terraform.io/providers/okta/okta/latest/docs/resources/user_admin_roles
#       https://registry.terraform.io/providers/okta/okta/latest/docs/resources/admin_role_targets

variable "app_owner_login" {
  type        = string
  default     = "${_tfEsc(ownerLogin)}"
  description = "Okta login (email) of the application owner"
}

data "okta_user" "app_owner" {
  # Look up the user by their Okta login attribute
  search {
    name       = "profile.login"
    value      = var.app_owner_login
    comparison = "eq"
  }
}

# Step 1 — grant the APP_ADMIN role at org level
resource "okta_user_admin_roles" "${resName}_owner_role" {
  user_id     = data.okta_user.app_owner.id
  admin_roles = ["APP_ADMIN"]
}

# Step 2 — scope the role to this specific app only
resource "okta_admin_role_targets" "${resName}_owner_targets" {
  user_id   = data.okta_user.app_owner.id
  role_type = "APP_ADMIN"
  apps      = [${resType}.${resName}.id]

  depends_on = [
    ${resType}.${resName},
    okta_user_admin_roles.${resName}_owner_role,
  ]
}

`;
}

function _buildOutputsTf(resName, proto) {
  const isOidc  = proto === 'oidc';
  const resType = isOidc ? 'okta_app_oauth' : 'okta_app_saml';

  const oidcOutputs = isOidc ? `
output "client_id" {
  value       = ${resType}.${resName}.client_id
  description = "OAuth 2.0 client_id"
}

output "client_secret" {
  value       = ${resType}.${resName}.client_secret
  sensitive   = true
  description = "OAuth 2.0 client_secret — store in a secret manager, never in source code"
}` : '';

  return `# ─── Outputs ──────────────────────────────────────────────────────────────────
output "app_id" {
  value       = ${resType}.${resName}.id
  description = "Okta application ID"
}
${oidcOutputs}
`;
}

